function seperatorString(arr, seperator){
    var end_string = ""

    if (arr.length == 0){
        return "";
    }
    else if (arr.length == 1){
        return end_string + (arr[0])
    }
    else {
        for (var i = 0; i < arr.length; i++){
            if (i < arr.length-1){
                end_string += arr[i] + seperator;
            }
            else {
                end_string += arr[i]
            }
        }
    }
    return end_string;
}

